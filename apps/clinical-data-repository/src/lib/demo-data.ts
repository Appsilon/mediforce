export function getFileById(fileId: string): RequirementFile | StudyFile | undefined {
  return (
    requirementFiles.find((f) => f.id === fileId) ??
    studyFiles.find((f) => f.id === fileId)
  );
}

export function getValidationIssuesForFile(fileId: string): ValidationIssue[] {
  return validationIssues.filter((issue) => issue.fileId === fileId);
}

export function getTotalIssueCounts() {
  const errors = validationIssues.filter((i) => i.severity === 'Error').length;
  const warnings = validationIssues.filter((i) => i.severity === 'Warning').length;
  const info = validationIssues.filter((i) => i.severity === 'Info').length;
  return { errors, warnings, info, total: validationIssues.length };
}
